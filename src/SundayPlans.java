public class SundayPlans {
    private String sleep;
    private String washDresses;

    public SundayPlans(String sleep, String washDresses) {
        this.sleep = sleep;
        this.washDresses = washDresses;
    }

    public String getSleep() {
        return sleep;
    }

    public String getWashDresses() {
        return washDresses;
    }
}