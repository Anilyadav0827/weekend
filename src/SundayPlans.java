public class SundayPlans {
    private String party;
    private String completeWork;

    public SundayPlans(String party, String completeWork) {
        this.party = party;
        this.completeWork = completeWork;
    }

    public String getParty() {
        return party;
    }

    public String getCompleteWork() {
        return completeWork;
    }
}